import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { build } from 'esbuild';
import * as prettier from 'prettier';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const appRoot = path.resolve(scriptDir, '..');
const repoRoot = path.resolve(appRoot, '..', '..');

const runtimeSources = [
  {
    assetPath: path.join(
      appRoot,
      'src/features/artifacts/runtime-preview/js/document.asset.html',
    ),
    generatedPath: path.join(
      appRoot,
      'src/features/artifacts/runtime-preview/js/document-source.ts',
    ),
    exportName: 'JS_ARTIFACT_RUNTIME_DOCUMENT_SOURCE',
  },
];

const bundledRuntimeSources = [
  {
    entryPath: path.join(
      appRoot,
      'src/features/assistant/runtime-persistence/artifact-runtime-persistence-bootstrap-entry.ts',
    ),
    generatedPath: path.join(
      appRoot,
      'src/features/assistant/runtime-persistence/artifact-runtime-persistence-bootstrap-source.ts',
    ),
    exportName: 'ARTIFACT_RUNTIME_PERSISTENCE_BOOTSTRAP_SOURCE',
  },
];

const generatedModuleGroups = [
  {
    generatedPath: path.join(
      appRoot,
      'src/features/artifacts/runtime-preview/react-bundle/runtime-module-sources.js',
    ),
    entries: [
      {
        exportName: 'REACT_ARTIFACT_RUNTIME_MODULE_SOURCES',
        sourceMap: {
          react: path.join(
            repoRoot,
            'node_modules/react/cjs/react.production.js',
          ),
          'react-dom': path.join(
            repoRoot,
            'node_modules/react-dom/cjs/react-dom.production.js',
          ),
          'react-dom/client': path.join(
            repoRoot,
            'node_modules/react-dom/cjs/react-dom-client.production.js',
          ),
          scheduler: path.join(
            repoRoot,
            'node_modules/scheduler/cjs/scheduler.production.js',
          ),
        },
      },
    ],
  },
];

function normalizeContent(value) {
  return value.replace(/\r\n/g, '\n');
}

async function formatGeneratedTypeScript(source, generatedPath) {
  const prettierOptions = await prettier.resolveConfig(generatedPath);
  return normalizeContent(
    await prettier.format(source, {
      ...prettierOptions,
      filepath: generatedPath,
    }),
  );
}

async function formatGeneratedSource(source, generatedPath) {
  const prettierOptions = await prettier.resolveConfig(generatedPath);
  return normalizeContent(
    await prettier.format(source, {
      ...prettierOptions,
      filepath: generatedPath,
    }),
  );
}

async function buildGeneratedModule(
  assetPath,
  generatedPath,
  exportName,
  contents,
) {
  return formatGeneratedTypeScript(
    [
      `// Generated from ${path.basename(assetPath)} by scripts/sync-artifact-runtime-sources.mjs.`,
      '// Do not edit this file by hand.',
      `export const ${exportName} = ${JSON.stringify(contents)};`,
      '',
    ].join('\n'),
    generatedPath,
  );
}

async function buildBundledRuntimeSource(entryPath) {
  const result = await build({
    absWorkingDir: appRoot,
    entryPoints: [entryPath],
    bundle: true,
    charset: 'utf8',
    format: 'iife',
    legalComments: 'none',
    minify: false,
    platform: 'browser',
    sourcemap: false,
    target: ['es2022'],
    treeShaking: false,
    write: false,
  });
  const [outputFile] = result.outputFiles ?? [];
  if (!outputFile) {
    throw new Error(
      `missing bundled runtime output for ${path.relative(appRoot, entryPath)}`,
    );
  }
  return normalizeContent(outputFile.text).trimEnd() + '\n';
}

async function buildGeneratedModuleGroup(group) {
  return formatGeneratedSource(
    [
      '// Generated by scripts/sync-artifact-runtime-sources.mjs.',
      '// Do not edit this file by hand.',
      ...group.entries.map((entry) => {
        const normalizedMap = Object.fromEntries(
          Object.entries(entry.sourceMap).map(([moduleId, sourcePath]) => [
            moduleId,
            normalizeContent(fs.readFileSync(sourcePath, 'utf8')),
          ]),
        );
        return `export const ${entry.exportName} = ${JSON.stringify(normalizedMap)};`;
      }),
      '',
    ].join('\n'),
    group.generatedPath,
  );
}

async function main() {
  const checkOnly = process.argv.includes('--check');
  const staleTargets = [];

  for (const runtimeSource of runtimeSources) {
    const assetContents = normalizeContent(
      fs.readFileSync(runtimeSource.assetPath, 'utf8'),
    );
    const expectedModule = await buildGeneratedModule(
      runtimeSource.assetPath,
      runtimeSource.generatedPath,
      runtimeSource.exportName,
      assetContents,
    );
    const currentModule = fs.existsSync(runtimeSource.generatedPath)
      ? normalizeContent(fs.readFileSync(runtimeSource.generatedPath, 'utf8'))
      : null;

    if (currentModule === expectedModule) {
      continue;
    }

    if (checkOnly) {
      staleTargets.push(path.relative(appRoot, runtimeSource.generatedPath));
      continue;
    }

    fs.writeFileSync(runtimeSource.generatedPath, expectedModule);
  }

  for (const bundledRuntimeSource of bundledRuntimeSources) {
    const bundledSource = await buildBundledRuntimeSource(
      bundledRuntimeSource.entryPath,
    );
    const expectedModule = await buildGeneratedModule(
      bundledRuntimeSource.entryPath,
      bundledRuntimeSource.generatedPath,
      bundledRuntimeSource.exportName,
      bundledSource,
    );
    const currentModule = fs.existsSync(bundledRuntimeSource.generatedPath)
      ? normalizeContent(
          fs.readFileSync(bundledRuntimeSource.generatedPath, 'utf8'),
        )
      : null;

    if (currentModule === expectedModule) {
      continue;
    }

    if (checkOnly) {
      staleTargets.push(
        path.relative(appRoot, bundledRuntimeSource.generatedPath),
      );
      continue;
    }

    fs.writeFileSync(bundledRuntimeSource.generatedPath, expectedModule);
  }

  for (const generatedModuleGroup of generatedModuleGroups) {
    const expectedModule =
      await buildGeneratedModuleGroup(generatedModuleGroup);
    const currentModule = fs.existsSync(generatedModuleGroup.generatedPath)
      ? normalizeContent(
          fs.readFileSync(generatedModuleGroup.generatedPath, 'utf8'),
        )
      : null;

    if (currentModule === expectedModule) {
      continue;
    }

    if (checkOnly) {
      staleTargets.push(
        path.relative(appRoot, generatedModuleGroup.generatedPath),
      );
      continue;
    }

    fs.writeFileSync(generatedModuleGroup.generatedPath, expectedModule);
  }

  if (checkOnly && staleTargets.length > 0) {
    console.error(
      [
        'artifact runtime source wrappers are out of date:',
        ...staleTargets.map((target) => `- ${target}`),
        'run `npm run sync:artifact-runtime-sources -w apps/web-shell`',
      ].join('\n'),
    );
    process.exitCode = 1;
  }
}

await main();
